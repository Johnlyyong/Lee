import java.util.Scanner;

class Act1 {
   public void start() {
      Scanner scan = new Scanner(System.in);
      int sum = 0;
      System.out.println("Input 10 integers: ");
      for (int i = 0; i < 10; i++) {
         int num = scan.nextInt();
         if (num > 0)
            sum += num;
      }
      System.out.println("\nYour sum is: " + sum);
   }
}
